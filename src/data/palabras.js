// FunciÃ³n para obtener palabras segÃºn dificultad
export const getPalabrasPorDificultad = (categoria, dificultad) => {
  const palabras = CATEGORIAS[categoria].palabras;

  switch(dificultad) {
    case 'facil':
      // Palabras mÃ¡s cortas y conocidas
      return palabras.filter(p => p.length <= 8).slice(0, 15);
    case 'medio':
      // Todas las palabras
      return palabras;
    case 'dificil':
      // Palabras mÃ¡s largas y menos comunes
      return palabras.filter(p => p.length >= 6);
    default:
      return palabras;
  }
};

